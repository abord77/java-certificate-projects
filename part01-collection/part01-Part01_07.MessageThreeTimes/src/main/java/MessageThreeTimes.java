
import java.util.Scanner;

public class MessageThreeTimes {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        System.out.println("Write a message:");
        String a = sc.nextLine();
        
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);

    }
}
